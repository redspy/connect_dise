/** 이미지 심볼 경로 (symbolIdx: 0~56) */
export function getSymbolPath(symbolIdx) {
  const fileNum = validSymbolIndices[symbolIdx];
  return `/games/dobble/assets/symbols/symbol_${String(fileNum).padStart(3, '0')}.png`;
}

/** Projective Plane Order 7 덱 생성 — 57장, 카드당 8심볼 */
export function generateDeck() {
  const n = SYMBOLS_PER_CARD - 1; // 7
  const cards = [];

  // 첫 n+1 장
  for (let i = 0; i <= n; i++) {
    const card = [0];
    for (let j = 0; j < n; j++) card.push(i * n + j + 1);
    cards.push(card);
  }

  // 나머지 n² 장
  for (let i = 0; i < n; i++) {
    for (let j = 0; j < n; j++) {
      const card = [i + 1];
      for (let k = 0; k < n; k++) card.push(n + 1 + n * k + (i * k + j) % n);
      cards.push(card);
    }
  }

  // 셔플
  for (let i = cards.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [cards[i], cards[j]] = [cards[j], cards[i]];
  }
  return cards;
}
